    public void Prestamos() {
        contador++;
        lblPre2.setText(Integer.toString(3 - contador));
        switch (contador) {
            case 0:
                JOptionPane.showMessageDialog(rootPane, "No puedes prestar mas musica", "Error 404 NOT FOUND", 0);
                break;
            case 1:
                for (int i = 0; i < 1; i++) {
                    valBoton();
                }
                fechaPre();
                break;
            case 2:
                for (int i = 0 + 1; i < 2; i++) {
                    valBoton();
                }
                fechaPre();
                break;
            case 3:
                for (int i = 0 + 1 + 1; i < 2 + 1; i++) {
                    valBoton();
                }
                fechaPre();
                break;
            default:
                JOptionPane.showMessageDialog(rootPane, "No puedes prestar mas musica", "Error 404 NOT FOUND", 0);
                lblPre2.setText(Integer.toString(0));
                break;
        }
    }

